POINTS = {
    ')': 3,
    ']': 57,
    '}': 1197,
    '>': 25137,
}


def solve(navigation_subsystem_str):
    stack = []

    score = 0

    for line in navigation_subsystem_str.split("\n"):
        for char in line:
            old_score = score
            if char in '([{<':
                stack.append(char)
            else:
                opened = stack[-1]
                if char == ']' and opened != '[':
                    score += POINTS[char]
                elif char == '}' and opened != '{':
                    score += POINTS[char]
                elif char == ')' and opened != '(':
                    score += POINTS[char]
                elif char == '>' and opened != '<':
                    score += POINTS[char]
                else:
                    stack.pop()

            if score > old_score:
                break

    return score


